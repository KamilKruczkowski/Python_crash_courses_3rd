print(f"Last four numbers: {numbers_2[-4:]}")
print(f"Reverse list: {numbers_2[::-1]}")
print()

num_3 = tuple(range(1,10))
print(f"Original tuple: {num_3}")
print(f"Elements from 4 to end: {num_3[3:]}")
print(f"Numbers from beginning to 5: {num_3[:5]}")
print(f"Numbers without first and last: {num_3[1:-1]}")