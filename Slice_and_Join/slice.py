numbers = list(range(1,11))
print(f' Original list: {numbers}')
print(f" First three: {numbers[0:3]}")
print(f" Last three: {numbers[-3:]}")
print(f" From second to before last: {numbers[1:-1]}")
print()

letters = list('abcdefgh')
print(f'Original letters: {letters}')
print(f"Every second letter from first: {letters[::2]}")
print(f"Every second letter from second: {letters[1::2]}")
print(f"From end to beginning: {letters[::-1]}")
print()

name = ('Python Programming')
print(f"Original name: {name}")
print(f"First word: {name[0:6]}")
print(f"Second word: {name[7:]}")
print(f"Every second letter: {name[::2]}")
print()

numbers_2 = list(range(1,11))
